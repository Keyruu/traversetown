package de.keyruu.traversetown.lastfm.pojo;

public class Stats
{
  private String listeners;
  private String playcount;
  private String userplaycount;

  public String getListeners()
  {
    return listeners;
  }

  public void setListeners(String value)
  {
    this.listeners = value;
  }

  public String getPlaycount()
  {
    return playcount;
  }

  public void setPlaycount(String value)
  {
    this.playcount = value;
  }

  public String getUserplaycount()
  {
    return userplaycount;
  }

  public void setUserplaycount(String value)
  {
    this.userplaycount = value;
  }
}
